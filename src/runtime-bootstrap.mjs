import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { execFileSync } from 'node:child_process';

const ENV_FILE_NAMES = ['.env', '.env.local'];
const IMPORTANT_RUNTIME_KEYS = [
  'PORT',
  'DATABASE_URL',
  'PGHOST',
  'PGPORT',
  'PGDATABASE',
  'PGUSER',
  'PGPASSWORD',
  'PSQL_BIN',
  'SCHOLAXIS_STORAGE_BACKEND',
  'SCHOLAXIS_VECTOR_BACKEND',
  'SCHOLAXIS_GRAPH_BACKEND',
  'SCHOLAXIS_DB_PATH',
  'SCHOLAXIS_VECTOR_SERVICE_URL',
  'SCHOLAXIS_GRAPH_SERVICE_URL',
  'SCHOLAXIS_LOCAL_MODEL_AUTOSTART',
  'SCHOLAXIS_LOCAL_MODEL_SERVICE_URL',
  'SCHOLAXIS_LOCAL_MODEL_PYTHON_BIN',
  'SCHOLAXIS_EMBEDDING_PROVIDER',
  'SCHOLAXIS_RERANKER_PROVIDER',
];

const bootstrapState = {
  initialized: false,
  cwd: '',
  persistEnvFile: false,
  loadedEnvFiles: [],
  createdEnvFile: '',
  envCreateError: '',
  inferredDefaults: [],
  localModel: {
    available: null,
    autoDisabled: false,
    reason: '',
  },
};

function parseEnvValue(rawValue = '') {
  const value = String(rawValue || '').trim();
  if (!value) return '';
  const quote = value[0];
  if ((quote === '"' || quote === '\'') && value.endsWith(quote)) {
    return value
      .slice(1, -1)
      .replace(/\\n/g, '\n')
      .replace(/\\r/g, '\r')
      .replace(/\\t/g, '\t')
      .replace(/\\"/g, '"')
      .replace(/\\'/g, '\'')
      .replace(/\\\\/g, '\\');
  }
  return value;
}

function loadEnvFile(filePath) {
  if (!existsSync(filePath)) return [];
  const contents = readFileSync(filePath, 'utf8');
  const loaded = [];
  for (const line of contents.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const normalized = trimmed.startsWith('export ') ? trimmed.slice(7).trim() : trimmed;
    const separatorIndex = normalized.indexOf('=');
    if (separatorIndex <= 0) continue;
    const key = normalized.slice(0, separatorIndex).trim();
    if (!key) continue;
    const value = parseEnvValue(normalized.slice(separatorIndex + 1));
    if (!(key in process.env)) {
      process.env[key] = value;
      loaded.push(key);
    }
  }
  return loaded;
}

function hasAnyEnvFile(cwd) {
  return ENV_FILE_NAMES.some((name) => existsSync(path.join(cwd, name)));
}

function hasExplicitRuntimeConfig() {
  return IMPORTANT_RUNTIME_KEYS.some((key) => key in process.env);
}

function localModelRequestedExplicitly() {
  const embeddingProvider = String(process.env.SCHOLAXIS_EMBEDDING_PROVIDER || '').trim().toLowerCase();
  const rerankerProvider = String(process.env.SCHOLAXIS_RERANKER_PROVIDER || '').trim().toLowerCase();
  return embeddingProvider === 'hybrid-local' || rerankerProvider === 'hybrid-local';
}

function pythonHasSentenceTransformers(binary = '') {
  if (!binary) return false;
  try {
    execFileSync(binary, ['-c', 'import importlib.util, sys; sys.exit(0 if importlib.util.find_spec("sentence_transformers") else 1)'], {
      stdio: 'ignore',
      timeout: 2500,
    });
    return true;
  } catch {
    return false;
  }
}

function detectLocalModelAvailability(cwd, probe) {
  if (typeof probe === 'function') {
    return Boolean(probe({ cwd, env: process.env }));
  }
  const explicitBinary = process.env.SCHOLAXIS_LOCAL_MODEL_PYTHON_BIN;
  if (explicitBinary) return pythonHasSentenceTransformers(explicitBinary);

  const repoVenvBinary = path.join(cwd, '.venv-local-models', 'bin', 'python');
  if (existsSync(repoVenvBinary) && pythonHasSentenceTransformers(repoVenvBinary)) return true;

  return pythonHasSentenceTransformers('python3');
}

function buildQuickstartEnvFile({ localModelAvailable = false } = {}) {
  return [
    '# Auto-generated by Scholaxis on first start for zero-touch quickstart.',
    '# Edit this file (or create .env.local) when you want deployment-specific overrides.',
    '# For the full reference, see .env.example.',
    '',
    'PORT=3000',
    'SCHOLAXIS_STORAGE_BACKEND=sqlite',
    'SCHOLAXIS_VECTOR_BACKEND=local',
    'SCHOLAXIS_GRAPH_BACKEND=local',
    'SCHOLAXIS_DB_PATH=.data/scholaxis.db',
    'SCHOLAXIS_ENABLE_LIVE_SOURCES=false',
    'SCHOLAXIS_AUTO_LIVE_ON_EMPTY=true',
    `SCHOLAXIS_LOCAL_MODEL_AUTOSTART=${localModelAvailable ? '1' : '0'}`,
    '',
    '# If you already have PostgreSQL ready, uncomment and fill these instead of the SQLite defaults above.',
    '# SCHOLAXIS_STORAGE_BACKEND=postgres',
    '# SCHOLAXIS_VECTOR_BACKEND=pgvector',
    '# DATABASE_URL=postgres://scholaxis:password@127.0.0.1:5432/scholaxis',
    '',
  ].join('\n');
}

function ensureQuickstartEnvFile(cwd, state, options = {}) {
  if (!state.persistEnvFile || hasAnyEnvFile(cwd) || hasExplicitRuntimeConfig()) return;
  const envPath = path.join(cwd, '.env');
  const contents = buildQuickstartEnvFile({
    localModelAvailable: detectLocalModelAvailability(cwd, options.localModelProbe),
  });
  const writer = typeof options.writeEnvFile === 'function'
    ? options.writeEnvFile
    : (targetPath, targetContents) => writeFileSync(targetPath, targetContents, 'utf8');
  try {
    writer(envPath, contents);
    state.createdEnvFile = envPath;
    state.envCreateError = '';
  } catch (error) {
    state.createdEnvFile = '';
    state.envCreateError = error?.message || String(error || 'env-create-failed');
  }
}

function inferRuntimeDefaults(cwd, state, options = {}) {
  const hasPostgresConnection = Boolean(process.env.DATABASE_URL || process.env.PGHOST || process.env.PGSERVICE);
  if (hasPostgresConnection && !('SCHOLAXIS_STORAGE_BACKEND' in process.env)) {
    process.env.SCHOLAXIS_STORAGE_BACKEND = 'postgres';
    state.inferredDefaults.push('DATABASE_URL/PG* detected → SCHOLAXIS_STORAGE_BACKEND=postgres');
  }
  if (hasPostgresConnection && !('SCHOLAXIS_VECTOR_BACKEND' in process.env)) {
    process.env.SCHOLAXIS_VECTOR_BACKEND = 'pgvector';
    state.inferredDefaults.push('DATABASE_URL/PG* detected → SCHOLAXIS_VECTOR_BACKEND=pgvector');
  }
  if (process.env.SCHOLAXIS_GRAPH_SERVICE_URL && !('SCHOLAXIS_GRAPH_BACKEND' in process.env)) {
    process.env.SCHOLAXIS_GRAPH_BACKEND = 'http';
    state.inferredDefaults.push('SCHOLAXIS_GRAPH_SERVICE_URL detected → SCHOLAXIS_GRAPH_BACKEND=http');
  }
  if (
    !hasPostgresConnection &&
    process.env.SCHOLAXIS_VECTOR_SERVICE_URL &&
    !('SCHOLAXIS_VECTOR_BACKEND' in process.env)
  ) {
    process.env.SCHOLAXIS_VECTOR_BACKEND = 'http';
    state.inferredDefaults.push('SCHOLAXIS_VECTOR_SERVICE_URL detected → SCHOLAXIS_VECTOR_BACKEND=http');
  }

  const localModelAvailable = detectLocalModelAvailability(cwd, options.localModelProbe);
  state.localModel.available = localModelAvailable;
  state.localModel.reason = localModelAvailable
    ? 'sentence-transformers runtime detected'
    : 'sentence-transformers runtime not detected';

  if (
    !('SCHOLAXIS_LOCAL_MODEL_AUTOSTART' in process.env) &&
    !process.env.SCHOLAXIS_LOCAL_MODEL_SERVICE_URL &&
    !localModelRequestedExplicitly() &&
    !localModelAvailable
  ) {
    process.env.SCHOLAXIS_LOCAL_MODEL_AUTOSTART = '0';
    state.localModel.autoDisabled = true;
    state.inferredDefaults.push('local sentence-transformers runtime not detected → SCHOLAXIS_LOCAL_MODEL_AUTOSTART=0');
  }
}

export function bootstrapRuntime(options = {}) {
  if (bootstrapState.initialized) return bootstrapState;

  const cwd = path.resolve(options.cwd || process.cwd());
  bootstrapState.initialized = true;
  bootstrapState.cwd = cwd;
  bootstrapState.persistEnvFile = Boolean(options.persistEnvFile);
  bootstrapState.loadedEnvFiles = [];
  bootstrapState.createdEnvFile = '';
  bootstrapState.envCreateError = '';
  bootstrapState.inferredDefaults = [];
  bootstrapState.localModel = {
    available: null,
    autoDisabled: false,
    reason: '',
  };

  ensureQuickstartEnvFile(cwd, bootstrapState, options);
  for (const name of ENV_FILE_NAMES) {
    const filePath = path.join(cwd, name);
    const loadedKeys = loadEnvFile(filePath);
    if (loadedKeys.length) {
      bootstrapState.loadedEnvFiles.push(filePath);
    }
  }
  inferRuntimeDefaults(cwd, bootstrapState, options);

  return bootstrapState;
}

export function getBootstrapDiagnostics() {
  return bootstrapState;
}
