import { appConfig } from './config.mjs';
import { getAllGraphEdges, getStoredDocuments, listBackgroundJobs } from './storage.mjs';
import { toPgvectorLiteral } from './vector-index-service.mjs';

function sqlString(value) {
  if (value == null) return 'NULL';
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlJson(value) {
  return `${sqlString(JSON.stringify(value ?? null))}::jsonb`;
}

export function buildPostgresMigrationSql() {
  const documents = getStoredDocuments();
  const edges = getAllGraphEdges(10000);
  const jobs = listBackgroundJobs(1000);

  const statements = [
    '-- PostgreSQL + pgvector migration bundle generated by Scholaxis',
    'CREATE EXTENSION IF NOT EXISTS vector;',
    'CREATE TABLE IF NOT EXISTS documents (',
    '  canonical_id TEXT PRIMARY KEY,',
    '  source TEXT,',
    '  type TEXT,',
    '  title TEXT,',
    '  year INTEGER,',
    '  organization TEXT,',
    '  authors_json JSONB,',
    '  keywords_json JSONB,',
    '  summary TEXT,',
    '  links_json JSONB,',
    `  embedding vector(${appConfig.vectorDimensions}),`,
    '  sparse_json JSONB,',
    '  raw_json JSONB,',
    '  updated_at TIMESTAMPTZ',
    ');',
    `CREATE INDEX IF NOT EXISTS idx_documents_embedding_cosine ON documents USING hnsw (embedding vector_cosine_ops);`,
    'CREATE TABLE IF NOT EXISTS graph_edges (source_id TEXT, target_id TEXT, edge_type TEXT, weight DOUBLE PRECISION, created_at TIMESTAMPTZ);',
    'CREATE INDEX IF NOT EXISTS idx_graph_edges_source_type ON graph_edges(source_id, edge_type);',
    'CREATE TABLE IF NOT EXISTS background_jobs (id BIGSERIAL PRIMARY KEY, job_type TEXT, status TEXT, payload_json JSONB, priority INTEGER, attempts INTEGER, last_error TEXT, run_after TIMESTAMPTZ, leased_until TIMESTAMPTZ, created_at TIMESTAMPTZ, updated_at TIMESTAMPTZ, completed_at TIMESTAMPTZ);',
  ];

  for (const document of documents) {
    statements.push(
      `INSERT INTO documents (canonical_id, source, type, title, year, organization, authors_json, keywords_json, summary, links_json, embedding, sparse_json, raw_json, updated_at) VALUES (${sqlString(document.canonicalId)}, ${sqlString(document.source)}, ${sqlString(document.type)}, ${sqlString(document.title)}, ${document.year ?? 'NULL'}, ${sqlString(document.organization)}, ${sqlJson(document.authors)}, ${sqlJson(document.keywords)}, ${sqlString(document.summary)}, ${sqlJson(document.links)}, ${sqlString(toPgvectorLiteral(document.vector || []))}::vector, ${sqlJson(document.sparseVector || {})}, ${sqlJson(document.rawRecord)}, ${sqlString(document.updatedAt)});`
    );
  }

  for (const edge of edges) {
    statements.push(
      `INSERT INTO graph_edges (source_id, target_id, edge_type, weight, created_at) VALUES (${sqlString(edge.sourceId)}, ${sqlString(edge.targetId)}, ${sqlString(edge.edgeType)}, ${Number(edge.weight || 0)}, ${sqlString(edge.createdAt || new Date().toISOString())});`
    );
  }

  for (const job of jobs) {
    statements.push(
      `INSERT INTO background_jobs (id, job_type, status, payload_json, priority, attempts, last_error, run_after, leased_until, created_at, updated_at, completed_at) VALUES (${job.id}, ${sqlString(job.jobType)}, ${sqlString(job.status)}, ${sqlJson(job.payload)}, ${job.priority || 0}, ${job.attempts || 0}, ${sqlString(job.lastError || '')}, ${sqlString(job.runAfter)}, ${sqlString(job.leasedUntil)}, ${sqlString(job.createdAt)}, ${sqlString(job.updatedAt)}, ${sqlString(job.completedAt)});`
    );
  }

  return `${statements.join('\n')}\n`;
}
